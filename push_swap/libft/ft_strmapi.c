#include"libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*str;

	i = 0;
	if (!s || !f)
		return (NULL);
	while ((size_t)i < ft_strlen(s))
		i++;
	str = (char *) malloc (sizeof(char) * (i + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while ((size_t)i < ft_strlen(s))
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
